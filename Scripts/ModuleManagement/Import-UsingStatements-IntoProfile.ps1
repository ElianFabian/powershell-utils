# Import-UsingStatements-IntoProfile.ps1



# From repository: https://github.com/ElianFabian/powershell-utils



$REGEX_AUTOGENERATED_TEXT = "#<([\S\s]*?)>#"
$SEPARATOR                = "### ===================================================================================="
$GITHUB_REPOSITORY        = "https://github.com/ElianFabian/powershell-utils"

$modules = (Get-ChildItem -Path "../../Modules/AutogeneratedModules" -Recurse -File).BaseName

$usingModuleStatments = ""
foreach ($module in $modules)
{
    $usingModuleStatments += "using module $module`n"
}

$autogeneratedText = @(
    "#<### Modules imported with Import-UsingStatements-IntoProfile.ps1",
    "# Date when modules were added: $(Get-Date)",
    "# Repository of the modules:    $GITHUB_REPOSITORY",
    $SEPARATOR, '',
    $usingModuleStatments,
    "$SEPARATOR>#"
) -join "`n"


$previousContent          = Get-Content -Path $PROFILE -Raw
$autogeneratedTextMatches = (Select-String -InputObject $previousContent -Pattern $REGEX_AUTOGENERATED_TEXT -AllMatches).Matches

#region Prepend
if ($null -eq $autogeneratedTextMatches)
{
    $newContent = $autogeneratedText + $previousContent

    Set-Content -Path $PROFILE -Value $newContent
}
#endregion
#region Update
else
{
    foreach ($match in $autogeneratedTextMatches)
    {
        if (-not $match.Value.Contains($GITHUB_REPOSITORY)) { continue }

        $autogeneratedTextUpdated = $previousContent -replace $match.Value, $autogeneratedText

        Set-Content -Path $PROFILE -Value $autogeneratedTextUpdated -NoNewline

        break
    }
}
#endregion
